
public class Chapter09_Ex1 {
public static void main(String[] args) {
        
        for (int number = 1; number < 10; number++) {
            
            for (int i = 1; i < 10; i++) {
                
            System.out.print(number + "*" + i + "=" + number * i +" ");
            
            }
            
            System.out.println();
            
        }
        
    }

}
